# Input

stringval = input("Please enter a string \n")
print(f"This was your input: {stringval} \n")
print(type(stringval))

intval = int(input("Please enter an int \n"))
print(f"This was your input: {intval} \n")
print(type(intval))

floatval = float(input("Please enter a float \n"))
print(f"This was your input: {floatval} \n")
print(type(floatval))
